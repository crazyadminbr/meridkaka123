function formatProfile(user) {
  const regDate = user.registered_at
    ? new Date(user.registered_at).toLocaleString('ru-RU', { timeZone: 'UTC' })
    : 'Неизвестно';
  return `👤 <b>ВАШ ПРОФИЛЬ</b>

📅 Дата регистрации: <code>${regDate}</code>

<b>Информация о Telegram:</b>
• Юзернейм: ${user.username ? '@' + user.username : 'не указан'}
• Никнейм: ${user.full_name || 'не указан'}
• ID: <code>${user.telegram_id}</code>`;
}

